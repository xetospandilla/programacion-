/*
-------------------------------------------------------------------------------------------------------
Bucle Do-While
-------------------------------------------------------------------------------------------------------
- Utilizado cuando necesitas realizar una operación un número indeterminado de veces
y la condición de parada depende de algún cálculo o entrada del usuario que ocurre dentro
del bucle.

- Pero además necesita que el cuerpo del bucle se ejecute al menos una vez.

- Sintaxis: do{ cuerpodel bucle }while(condición a evaluar);
-------------------------------------------------------------------------------------------------------
*/